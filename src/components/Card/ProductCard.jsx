const ExpandMore = styled((props) => {
  const { expand, ...other } = props;
  return <IconButton {...other} />;
})(({ theme, expand }) => ({
  transform: !expand ? "rotate(0deg)" : "rotate(180deg)",
  marginLeft: "auto",
  transition: theme.transitions.create("transform", {
    duration: theme.transitions.duration.shortest,
  }),
}));

export default function ProductReviewCard(props) {
  const { product } = props;

  const ingredientsText = Object.keys(product).find((key) =>
    key.startsWith("ingredients_text_en_ocr_")
  );

  const theme = useTheme();
  const [activeStep, setActiveStep] = React.useState(0);
  const maxSteps = images.length;

  const handleNext = () => {
    setActiveStep((prevActiveStep) => prevActiveStep + 1);
  };

  const handleBack = () => {
    setActiveStep((prevActiveStep) => prevActiveStep - 1);
  };

  const handleStepChange = (step) => {
    setActiveStep(step);
  };

  return (
    <Box sx={{ flexGrow: 1, padding: 2 }}>
      <Card className="gradient-card">
        <CardHeader title={product.product_name} subheader={product.brands} />
        <AutoPlaySwipeableViews
          axis={theme.direction === "rtl" ? "x-reverse" : "x"}
          index={activeStep}
          onChangeIndex={handleStepChange}
          enableMouseEvents
        >
          {images.map((step, index) => (
            <div key={step.label}>
              {Math.abs(activeStep - index) <= 2 ? (
                <CardMedia
                  component="img"
                  height="194"
                  image={product.image_url}
                  alt={product.product_name}
                  sx={{ objectFit: "contain" }}
                  src={step.imgPath}
                />
              ) : null}
            </div>
          ))}
        </AutoPlaySwipeableViews>
        <MobileStepper
          steps={maxSteps}
          position="static"
          activeStep={activeStep}
          nextButton={
            <Button
              size="small"
              onClick={handleNext}
              disabled={activeStep === maxSteps - 1}
            >
              Next
              {theme.direction === "rtl" ? (
                <KeyboardArrowLeft />
              ) : (
                <KeyboardArrowRight />
              )}
            </Button>
          }
          backButton={
            <Button
              size="small"
              onClick={handleBack}
              disabled={activeStep === 0}
            >
              {theme.direction === "rtl" ? (
                <KeyboardArrowRight />
              ) : (
                <KeyboardArrowLeft />
              )}
              Back
            </Button>
          }
        />
        <CardContent className="gradient-card">
          {/* <Typography variant="body2" >
          Code : {product.code}
        </Typography>

        <Typography variant="body2"  sx={{ mt:1 }}>
          Categories : {product.categories_old}
        </Typography>

        <Typography variant="body2"  sx={{ mt:1 }}>
          Details: { ingredientsText && product[ingredientsText]}
        </Typography> */}
          <Grid container spacing={2}>
            <Grid item xs={12}>
              <Typography variant="h6">
                Code
              </Typography>
              <Typography gutterBottom>{product.code}</Typography>
            </Grid>
            <Grid item xs={12}>
              <Typography variant="h6">
                Categories
              </Typography>
              <Typography gutterBottom>{product.categories}</Typography>
            </Grid>
            <Grid item xs={12}>
              <Typography variant="h6">
                Details
              </Typography>
              <Typography gutterBottom>{ ingredientsText && product[ingredientsText]}</Typography>
            </Grid>
          </Grid>
        </CardContent>
      </Card>
    </Box>
  );
}
